-- 5. 对话表
CREATE TABLE IF NOT EXISTS chat_conversation (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         BIGINT NOT NULL,
    dataset_id      BIGINT NOT NULL,
    last_config_id  BIGINT,
    last_model_name VARCHAR(128),
    title           VARCHAR(255),
    is_pinned       BOOLEAN DEFAULT FALSE,
    created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- 6. 对话消息表（一行一轮：query + answer 同行）
CREATE TABLE IF NOT EXISTS chat_message (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    conversation_id     BIGINT NOT NULL,
    -- 轮次幂等键（前端每轮稳定 UUID）；列结构归 Python migration 0023，本地/测试 schema 与之保持一致。
    turn_id             VARCHAR(64),
    config_id           BIGINT,
    model_name          VARCHAR(128),
    `query`             TEXT,
    answer              TEXT,
    -- 生产 MySQL 为 JSON 列；H2 测试库降级为 VARCHAR 存 JSON 文本，与 MyBatis JacksonTypeHandler 行为一致（见 dataset_parse_config）。
    `references`        VARCHAR,
    request_id          VARCHAR(64),
    status              VARCHAR(16) NOT NULL DEFAULT 'GENERATING',
    error_code          VARCHAR(64),
    error_message       VARCHAR(512),
    created_at          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 7. LLM 调用用量日志表
CREATE TABLE IF NOT EXISTS llm_usage_log (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT NOT NULL,
    config_id           BIGINT,
    provider_type       VARCHAR(32) NOT NULL,
    model_name          VARCHAR(128) NOT NULL,
    stage               VARCHAR(16) NOT NULL,
    operation           VARCHAR(16) NOT NULL,
    prompt_tokens       INT NOT NULL,
    completion_tokens   INT NOT NULL,
    total_tokens        INT NOT NULL,
    latency_ms          INT,
    status              VARCHAR(16) NOT NULL,
    error_message       VARCHAR(512),
    created_at          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);


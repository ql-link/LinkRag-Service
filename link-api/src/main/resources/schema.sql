-- H2 Test Database Schema (MySQL Mode)

-- 1. 系统用户表
CREATE TABLE IF NOT EXISTS sys_user (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username        VARCHAR(64) NOT NULL,
    password_hash   VARCHAR(255) NOT NULL,
    nickname        VARCHAR(64),
    email           VARCHAR(128),
    phone           VARCHAR(20),
    avatar_url      VARCHAR(512),
    role            VARCHAR(32) NOT NULL DEFAULT 'USER',
    status          TINYINT NOT NULL DEFAULT 1,
    last_login_at   DATETIME,
    created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- 2. LLM 系统级厂商配置表
CREATE TABLE IF NOT EXISTS llm_system_provider (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_type   VARCHAR(32) NOT NULL,
    provider_name   VARCHAR(64) NOT NULL,
    icon_url        VARCHAR(512),
    icon_object_key VARCHAR(256),
    api_base_url    VARCHAR(512) NOT NULL,
    default_protocol VARCHAR(32) NOT NULL DEFAULT 'openai',
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    priority        INT NOT NULL DEFAULT 50,
    created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- 2.1 厂商模型能力目录表
CREATE TABLE IF NOT EXISTS llm_provider_model (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id     BIGINT NOT NULL,
    model_name      VARCHAR(128) NOT NULL,
    display_name    VARCHAR(64),
    capability      VARCHAR(32) NOT NULL,
    protocol        VARCHAR(32),
    api_base_url    VARCHAR(512),
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS llm_provider_model_sync_job (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id     BIGINT NOT NULL,
    sync_source     VARCHAR(32) NOT NULL,
    status          VARCHAR(16) NOT NULL,
    added_count     INT NOT NULL DEFAULT 0,
    updated_count   INT NOT NULL DEFAULT 0,
    stale_count     INT NOT NULL DEFAULT 0,
    error_message   VARCHAR(512),
    started_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    finished_at     DATETIME
);

CREATE TABLE IF NOT EXISTS llm_provider_model_sync_candidate (
    id                          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id                      BIGINT NOT NULL,
    provider_id                 BIGINT NOT NULL,
    sync_source                 VARCHAR(32) NOT NULL,
    external_model_id           VARCHAR(192) NOT NULL,
    model_name                  VARCHAR(128) NOT NULL,
    display_name                VARCHAR(64),
    inferred_capability         VARCHAR(32) NOT NULL,
    inferred_protocol           VARCHAR(32),
    inferred_api_base_url       VARCHAR(512),
    context_window              INT,
    max_output_tokens           INT,
    model_release_date          DATE,
    input_modalities            VARCHAR(1024),
    output_modalities           VARCHAR(1024),
    raw_metadata                CLOB,
    review_status               VARCHAR(16) NOT NULL DEFAULT 'PENDING',
    matched_provider_model_id   BIGINT,
    last_seen_at                DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at                  DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at                  DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- 2.2 系统预设表
